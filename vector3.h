#if ! defined VECTOR3_H
#define VECTOR3_H

#include <stdbool.h>

typedef union vector3 {
    double v[3];

    struct
    {
        double x;
        double y;
        double z;
    };
} Vector3;



//===============================================================================================================
// Functions to create vector3 objects
//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_from_zeroes(void);

//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_from_values(double x, double y, double z);

//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_from_points(Vector3 from, Vector3 to);

//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_from_array(double *buffer);



//===============================================================================================================
// Simple operations on single vector
//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_scalar_div( Vector3 vec, double scalar );

//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_scalar_mul( Vector3 vec, double scalar );

//---------------------------------------------------------------------------------------------------------------
extern double vec3_len_squared(Vector3 vec);

//---------------------------------------------------------------------------------------------------------------
extern double vec3_len(Vector3 vec);

//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_norm(Vector3 vec);

//---------------------------------------------------------------------------------------------------------------
extern void vec3_print(Vector3 a);



//===============================================================================================================
// Operations on one vector and at least one other object
//---------------------------------------------------------------------------------------------------------------
extern double vec3_dot(Vector3 a, Vector3 b);

//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_cross(Vector3 a,Vector3 b);

//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_add(Vector3 a, Vector3 b);

//---------------------------------------------------------------------------------------------------------------
extern bool vec3_equal(Vector3 a, Vector3 b);

//---------------------------------------------------------------------------------------------------------------
extern Vector3 vec3_project_plane(Vector3 vec, Vector3 normal);



#endif      // VECTOR3_H





